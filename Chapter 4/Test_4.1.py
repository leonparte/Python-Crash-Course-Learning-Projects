# 遍历整个列表

players = ['Chalmers', 'Wade', 'James', 'Battier', 'Bosh', 'Allen']

# 最简单的遍历
print("\n-----------------------------------------\n")

for player in players:
    print(player)

# 在循环中执行更多操作
print("\n-----------------------------------------\n")

for player in players:
    print("Thank you,", player.title(), "!")

# 在for循环结束后执行一些操作
print("\n-----------------------------------------\n")

for player in players:
    print("Thank you,", player.title(), "!")

print("\nThank you！ Heat Champion members !")

print("\n-----------------------------------------\n")