# 访问列表元素&索引从0而不是1开始

players = ['Wade', 'James', 'Bosh', 'Allen', 'Battier']

print("\n-----------------------------------------\n")

print(players)
# 索引从0开始：
print("My favorite player is", players[0].title(), '.')
# 使用索引“-1”可以便捷的返回最后一个列表元素
print("The player who used to play for Houston is", players[-1], '.')

print("\n-----------------------------------------\n")