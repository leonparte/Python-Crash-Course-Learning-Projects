# 使用方法修改字符串大小写

name = "i aM LeOnPArtE"

print("----------------------------------------------------\n")

# 输出所定义字符串：
print("The original text is: ", name)
# 输出首字母大写的字符串：
print("The original.title() text is: ", name.title())
# 输出全大写字符串：
print("The original.upper() text is: ", name.upper())
# 输出全小写字符串：
print("The original.lower() text is: ", name.lower())

print("\n----------------------------------------------------")